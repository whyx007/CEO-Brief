"""
CrewAI Agent 定义 — 6 个专业角色
"""

from crewai import Agent, LLM
from tools import (
    CompetitorSearchTool,
    BiddingSearchTool,
    FundingSearchTool,
    TalentSearchTool,
    TechSearchTool,
    DeepReadTool
)
from config import (
    DEEPSEEK_API_KEY, DEEPSEEK_BASE_URL, DEEPSEEK_MODEL,
    QWEN_API_KEY, QWEN_BASE_URL, QWEN_MODEL
)


# ──────────────────────────────────────
# DeepSeek LLM（OpenAI 兼容）
# ──────────────────────────────────────
deepseek_llm = LLM(
    model=f"openai/{DEEPSEEK_MODEL}",
    base_url=DEEPSEEK_BASE_URL,
    api_key=DEEPSEEK_API_KEY,
    temperature=0.15,
    max_tokens=8192,  # 常规任务的输出长度限制
    timeout=180,  # 3分钟超时（避免长时间等待）
)

# CEO 参谋专用 LLM（用于生成长报告）
deepseek_llm_long = LLM(
    model=f"openai/{DEEPSEEK_MODEL}",
    base_url=DEEPSEEK_BASE_URL,
    api_key=DEEPSEEK_API_KEY,
    temperature=0.15,
    max_tokens=32768,  # 生成完整长报告时使用更大的输出长度限制
    timeout=300,  # 5分钟超时（长报告需要更多时间）
)

# ──────────────────────────────────────
# 阿里 Qwen LLM
# ──────────────────────────────────────
qwen_llm = LLM(
    model=QWEN_MODEL,
    base_url=QWEN_BASE_URL,
    api_key=QWEN_API_KEY,
    temperature=0.15,
    max_tokens=32768,
    timeout=300,  # 5分钟超时
)


# ──────────────────────────────────────
# Agent 1: 情报侦察员
# ──────────────────────────────────────
intelligence_scout = Agent(
    role="竞情侦察员",
    goal=(
        "从公开渠道全面搜集中科天塔确认15-20家竞情目标（包括客户、竞争对手、潜在客户）在最近7天内的"
        "人才、市场、技术、竞标、融资五个维度的情报信息。"
        "你必须对每家种子公司调用搜索工具验证其最新动态。"
    ),
    backstory=(
        "你是一名资深的商业航天竞争情报分析师，精通中英文互联网信息检索。"
        "对每家种子公司调用 competitor_search 工具搜索。"
        "你擅长从新闻、招聘网站、招投标公告、融资公告、专利数据库、"
        "行业报告中提取关键竞情信息。你对卫星测控、在轨管理、航天软件有深入理解。"
        "**重要**：竞情目标包括客户、竞争对手、潜在客户。客户的自建体系、技术升级可能影响我们的业务；"
        "竞争对手的动向直接影响市场竞争格局。你需要区分不同类型目标的影响。"
        "**绝对不要编造信息，所有结论必须来自搜索结果。**"
    ),
    tools=[CompetitorSearchTool()],
    llm=deepseek_llm,
    verbose=True,
    allow_delegation=False,
    max_iter=30,
)


# ──────────────────────────────────────
# Agent 2: 人才分析师
# ──────────────────────────────────────
talent_analyst = Agent(
    role="人才团队分析师",
    goal=(
        "深度分析各竞情目标的人才团队动态：核心高管变动、关键技术岗位"
        "（测控/在轨管理/AI算法/架构师）招聘密度、组织架构调整。"
        "区分客户和竞争对手：客户的人才扩张可能意味着自建体系；竞争对手的人才扩张意味着竞争加剧。"
    ),
    backstory=(
        "你是一名专注航天领域的人力资源情报专家。你能从招聘信息的"
        "岗位名称、任职要求、薪资范围中判断一家公司的技术方向和"
        "扩张意图。对每家公司调用 talent_search 搜索招聘和人事信息。"
        "等关键岗位的人事变动。输出严格的 JSON 格式。"
        "不要编造不存在的招聘信息。**"
    ),
    tools=[TalentSearchTool(), CompetitorSearchTool(), DeepReadTool()],
    llm=deepseek_llm,
    verbose=True,
    allow_delegation=False,
    max_iter=20,
)


# ──────────────────────────────────────
# Agent 3: 市场分析师
# ──────────────────────────────────────
market_analyst = Agent(
    role="市场客户分析师",
    goal=(
        "追踪各竞情目标的市场拓展动态：新签客户、战略合作、"
        "进入新应用场景（低轨星座/遥感/导航/通信/军工/政府）、"
        "客户重叠度分析、市场份额变化。"
        "特别关注：我们的客户是否在与竞争对手合作，竞争对手是否在抢夺我们的客户。"
    ),
    backstory=(
        "你是一名商业航天市场研究专家，深谙卫星运营商、卫星星座公司、"
        "军工集团、政府机构的采购决策链。你能从合作公告、交付案例、"
        "展会信息中判断竞情目标的市场进展和客户抢夺风险。"
    ),
    tools=[CompetitorSearchTool(), BiddingSearchTool(), DeepReadTool()],
    llm=deepseek_llm,
    verbose=True,
    allow_delegation=False,
    max_iter=20,
)


# ──────────────────────────────────────
# Agent 4: 技术分析师
# ──────────────────────────────────────
tech_analyst = Agent(
    role="技术方向分析师",
    goal=(
        "深入分析各竞情目标的技术能力演进：在轨管理平台化进展、"
        "AI/大模型能力（是否对标天塔'华山'大模型）、测控自动化、"
        "数字孪生/仿真、软硬一体化、专利布局、学术产出。"
        "特别关注：客户的技术自建能力提升可能减少对天塔的依赖；竞争对手的技术突破直接威胁天塔优势。"
    ),
    backstory=(
        "你是一名航天系统工程技术专家，对卫星测控、在轨管理、"
        "飞行控制、任务规划、轨道计算等核心技术有深入理解。"
        "你能评估竞情目标的技术路线是否对天塔构成直接威胁，"
        "特别是在平台化和智能化方向上。"
        "**URL真实性铁律**："
        "- 所有返回的技术动态必须有真实可访问的URL"
        "- 如果搜索结果中没有URL，不要返回该条目"
        "- 绝对禁止编造URL或使用占位符"
        "- 宁可减少条目数量，也不要返回没有URL的信息"
    ),
    tools=[TechSearchTool(), CompetitorSearchTool(), DeepReadTool()],
    llm=deepseek_llm,
    verbose=True,
    allow_delegation=False,
    max_iter=20,
)


# ──────────────────────────────────────
# Agent 5: 竞标 & 融资分析师
# ──────────────────────────────────────
bidding_funding_analyst = Agent(
    role="竞标与融资分析师",
    goal=(
        "监控各竞情目标的招投标动态（中标/入围/大额项目/军采资质）"
        "和融资资本动态（新一轮融资/国资平台/估值变化/IPO进展），"
        "评估对天塔的直接竞争威胁或合作机会。"
    ),
    backstory=(
        "你是一名精通航天产业投融资和政府采购的分析师。"
        "你能从中标公告中判断项目的战略价值，从融资公告中"
        "判断竞情目标的资金弹药和后续扩张能力。"
    ),
    tools=[BiddingSearchTool(), FundingSearchTool(), CompetitorSearchTool()],
    llm=deepseek_llm,
    verbose=True,
    allow_delegation=False,
    max_iter=20,
)


# ──────────────────────────────────────
# Agent 6: 政策分析师
# ──────────────────────────────────────
